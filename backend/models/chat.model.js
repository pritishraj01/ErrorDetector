import mongoose from "mongoose"

const chatSchema= new mongoose.Schema({
    title:{
        type:String,
        required:true
    },
    response:{
        type:Object,
        required:true
    },
    owner:{
        type: mongoose.Schema.Types.ObjectId,
        ref:"User",
        required:true
    },
},{timestamps:true})

const Chat= mongoose.model("Chat",chatSchema)

export default Chat